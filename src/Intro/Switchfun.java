package Intro;

public class Switchfun 
{
	public static void main(String[] args) 
	{
		int no=1;
		
		switch(no)
		{
		case 1:System.out.println("Im one");
		case 2:System.out.println("Im two");
		break;
		case 3:System.out.println("Im three");
		default:System.out.println("I'm default");
		}
	}
}
